  <h4>HIP FRACTURES</h4>
  A hip fracture is a break in the upper quarter of the femur (thigh) bone. The extent of the break depends on the forces that are involved. The type of surgery used to treat a hip fracture is primarily based on the bones and soft tissues affected or on the level of the fracture.<br>
  <br>
  <br>

  <h5>Normal Anatomy of the Hip</h5>
  The "hip" is a ball-and-socket joint. It allows the upper leg to bend and rotate at the pelvis. An injury to the socket, or acetabulum, itself is not considered a "hip fracture." Management of fractures to the socket is a completely different consideration.<br>
  <br>

  <h5>Causes</h5>
  Hip fractures most commonly occur from a fall or from a direct blow to the side of the hip. Some medical conditions such as osteoporosis, cancer, or stress injuries can weaken the bone and make the hip more susceptible to breaking. In severe cases, it is possible for the hip to break with the patient merely standing on the leg and twisting.<br>
  <br>

  <h5>Symptoms</h5>
  The patient with a hip fracture will have pain over the outer upper thigh or in the groin. There will be significant discomfort with any attempt to flex or rotate the hip. If the bone has been weakened by disease (such as a stress injury or cancer), the patient may notice aching in the groin or thigh area for a period of time before the break. If the bone is completely broken, the leg may appear to be shorter than the noninjured leg. The patient will often hold the injured leg in a still position with the foot and knee turned outward (external rotation).<br>
  <br>

  <h5>Doctor Examination</h5>
  The diagnosis of a hip fracture is generally made by an X-ray of the hip and femur.<br>
  <br>

  Hip fractures occur at the upper end of the thigh bone (femur). In some cases, if the patient falls and complains of hip pain, an incomplete fracture may not be seen on a regular X-ray. In that case, magnetic resonance imaging (MRI) may be recommended. The MRI scan will usually show a hidden fracture.<br>
  <br>

  An MRI may identify a hip fracture otherwise missed on plain X-ray. If the patient is unable to have an MRI scan because of an associated medical condition, computed tomography (CT) may be obtained instead. Computed tomography, however, is not as sensitive as MRI for seeing hidden hip fractures.<br>
