  <h4>TYPES OF FRACTURES</h4>
  In general, there are three different types of hip fractures. The type of fracture depends on what area of the upper femur is involved.<br>
  <br>

  <h5>Intracapsular Fracture</h5>
  These fractures occur at the level of the neck and the head of the femur, and are generally within the capsule. The capsule is the soft-tissue envelope that contains the lubricating and nourishing fluid of the hip joint itself.<br>
  <br>

  <img src="images/hip3.jpg" alt=""><br>
  <br>

  <h5>Intertrochanteric Fracture</h5>
  This fracture occurs between the neck of the femur and a lower bony prominence called the lesser trochanter. The lesser trochanter is an attachment point for one of the major muscles of the hip. Intertrochanteric fractures generally cross in the area between the lesser trochanter and the greater trochanter. The greater trochanter is the bump you can feel under the skin on the outside of the hip. It acts as another muscle attachment point.<br>
  <br>

  <h5>Subtrochanteric Fracture</h5>
  This fracture occurs below the lesser trochanter, in a region that is between the lesser trochanter and an area approximately 2 1/2 inches belowf. This occurs even further down the bone and may be broken into several pieces.<br>
  <br>

