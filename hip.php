  <h5>After Surgery</h5>
  Patients may be discharged from the hospital to their home or find that a stay in a rehabilitation facility is necessary to assist them in regaining their ability to walk.<br>
  <br>

  <h5>Rehabilitation</h5>
  Patients may be encouraged to get out of bed on the day following surgery with the assistance of a physical therapist. The amount of weight that is allowed to be placed on the injured leg will be determined by the surgeon and is generally a function of the type of fracture and repair (fixation).<br>
  <br>

  The physical therapist will work with the patient to help regain strength and the ability to walk. This process may take up to three months.<br>
  <br>

  <strong>Medical Care:</strong> Occasionally, a blood transfusion may be required after surgery, but longer term antibiotics are generally not necessary. Most patients will be placed on medicine to thin their blood to reduce the chances of developing blood clots for up to 6 weeks. These medicines may be in the form of pills or injections. Elastic compression stockings or inflatable compression boots may also be used.<br>
  <br>

  <h5>Follow Up Care</h5>
  During the appointments that take place after surgery, the surgeon will want to check the wound, remove sutures, follow the healing process using X-rays, and prescribe additional physical therapy, if necessary. Following hip fracture surgery, most patients will regain much, if not all, of the mobility and independence they had before the injury.
