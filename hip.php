  <br>
  <br>

  <h4>TREATMENT OPTIONS</h4>
  In more complicated cases, the amount of breakage of the bone can involve more than one of these zones. This is taken into consideration when surgical repair is considered.<br>
  <br>

  <h5>Considerations</h5>
  Once the diagnosis of the hip fracture has been made, the patient's overall health and medical condition will be evaluated. In very rare cases, the patient may be so ill that surgery would not be recommended. In these cases, the patient's overall comfort and level of pain must be weighed against the risks of anesthesia and surgery. Most surgeons agree that patients do better if they are operated on fairly quickly. It is, however, important to insure patients' safety and maximize their overall medical health before surgery. This may mean taking time to do cardiac and other diagnostic studies.<br>
  <br>

  <h5>Nonsurgical Treatment</h5>
  Stable Impacted Fracture. Certain fractures that have not moved ("displaced") may not require surgery. Because there is a risk that they may move later on, they are often fixed.<br>
  <br>

  Patients who might be considered for nonsurgical treatment include those who are too ill to undergo any form of anesthesia and people who were unable to walk before their injury and may have been confined to a bed or a wheelchair.<br>
  <br>

  Certain types of fractures may be considered stable enough to be managed with nonsurgical treatment. Because there is some risk that these "stable" fractures may instead prove unstable and displace (change position), the doctor will need to follow with periodic X-rays of the area. If patients are confined to bed rest as part of the management for these fractures, they will need to be closely monitored for complications that can occur from prolonged immobilization. These include infections, bed sores, pneumonia, the formation of blood clots, and nutritional wasting.<br>
  <br>

  <img src="images/hip5.jpg" alt=""><br>
  <br>

  <h5>Surgical Treatment</h5>
  <strong>Before Surgery:</strong> Anesthesia for surgery could be either general anesthesia with a breathing tube or spinal anesthesia. In very rare circumstances, where only a few screws are planned for fixation, local anesthesia with heavy sedation can be considered. All patients will receive antibiotics during surgery and for the 24-hours afterward.<br>
  <br>

  Appropriate blood tests, chest X-rays, electrocardiograms, and urine samples will be obtained before surgery. Many elderly patients may have undiagnosed urinary tract infections that could lead to an infection of the hip after surgery. The surgeon's decision as to how to best fix a fracture will be based on the area of the hip that is broken and the surgeon's familiarity with the different systems that are available to manage these injuries.<br>
  <br>

  <strong>Intracapsular Fracture:</strong> If the head of the femur ("ball") alone is broken, management will be aimed at fixing the cartilage on the ball that has been injured or displaced. Frequently with these injuries, the socket, or acetabulum, may also be broken. The surgeon will need to take this into consideration as well.<br>
  <br>

  These injuries may be approached either from either the front or back of the hip. In some cases, both approaches are required in order to clearly see and fix the injured bone.<br>
  <br>

  For true intracapsular hip fractures, the surgeon may decide either to fix the fracture with individual screws (percutaneous pinning) or a single larger screw that slides within the barrel of a plate. This compression hip screw will allow the fracture to become more stable by having the broken area impact on itself. Occasionally, a secondary screw may be added for stability.<br>
  <br>

  Repair of an intracapsular fracture with individual screws (<strong>left</strong>) and of an intracapsular fracture with a single compression hip screw (<strong>right</strong>).<br>
  <br>

  <img src="images/hip6.jpg" alt=""><br>
  <br>

  If the intracapsular hip fracture is displaced in a younger patient, a surgical attempt will be made to reduce, or realign, the fracture through a larger incision. The fracture will be held together with either individual screws or with the larger compression hip screw. In these cases, the blood supply to the ball, or head of the femur, may have been damaged at the time of injury (avascular necrosis). Even though the fracture is realigned and fixed into place, the cartilage and underlying supporting bone may not receive adequate blood. Over a period of time, this may cause the femoral head to flatten out. When this occurs, the joint surface becomes irregular. Ultimately, the hip joint may develop a painful arthritis, despite the surgical repair.<br>
  <br>

  <img src="images/hip7.jpg" alt=""><br>
  <br>

  Although the fracture is repaired, the blood supply to the "ball" of the femur is damaged. In the older patient, the chance that the head of the femur is damaged in this way is higher. It is generally felt that for these displaced fractures, patients will do better if some of the components of the hip are replaced. In some cases, this can mean a replacement of the ball, or head of the femur (hemiarthroplasty). In other cases, this can mean the replacement of both the ball and socket, or head of the femur and acetabulum (total hip replacement).<br>
