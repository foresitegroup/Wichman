  <strong>Intertrochanteric Fracture:</strong> Repair of an intertrochanteric fracture with an intramedullary nail. The nail is in the hollow cavity of the femur (thighbone) rather than on the side of it (as with a plate).<br>
  <br>

  <img src="images/hip10.jpg" alt=""><br>
  <br>

  Most intertrochanteric fractures are managed with either a compression hip screw or an intramedullary nail, which also allows for impaction at the fracture site.<br>
  <br>

  The compression hip screw is fixed to the outer side of the bone with bone screws and has a large secondary screw (lag screw) that is placed through the plate into the neck and head of the hip (see compression hip screw figure above). The design of the device allows for impaction and compression at the fracture site. This may increase the stability of the area and promote healing.<br>
  <br>

  The intramedullary nail is placed directly into the marrow canal of the bone through an opening made at the top of the greater trochanter. A lag screw is then placed through the nail and up into the neck and head of the hip. As with the compression hip screw, sliding of the lag screw and impaction of the fracture take place.<br>
  <br>

  There are no definitive studies to show that one device is superior to another. The decision as to which to use is based on the surgeon's preference and experience.<br>
  <br>

  <strong>Subtrochanteric Fracture:</strong> At the subtrochanteric level, most fractures are managed with a long intramedullary nail together with a large lag screw or they are managed with screws that capture the neck and head of the femur or the area immediately underneath it, if it has remained intact.<br>
  <br>

  <img src="images/hip11.jpg" alt="Repair of subtrochanteric fracture with a long intramedullary nail."><br>
  <span style="font-size: 87.5%; line-height: 1.5em;">Repair of subtrochanteric fracture with a long intramedullary nail.</span><br>
  <br>

  Interlocking screws at the end of the nail make the fixation more secure. In order to keep the bones from rotating around the nail or from shortening ("telescoping") on the nail, additional screws may be placed at the lower end of the nail in the area of the knee. These are called interlocking screws.<br>
  <br>

  In certain cases, the surgeon may choose to use a plate rather than a nail. The plate will have screws that go into the bone from the lateral, or outer, side of the femur. A single large screw goes into the neck and the head of the femur and appears similar to the compression hip screw, but at a different angle. Secondary screws are then placed through the plate into the bone to hold the fracture in place.<br>
