  <h4>SHOULDER INSTABILITY</h4>
  Shoulder instability could be the result of a number of activites, although the most common shoulder injuries include Shoulder Subluxation, Shoulder Dislocation, or Shoulder Separation.<br>
  <br>
  <br>

  <img src="images/shoulder2.jpg" alt=""><br>
  <br>
  <br>

  <h5>SHOULDER SUBLUXATION</h5>
  This occurs when the shoulder doesn't fully come out of the socket, but slides part way out and then back in on its own. This is frequently seen in active, younger patients that are "double-jointed" with loose ligaments.<br>
  <br>

  <h5>SHOULDER DISLOCATION</h5>
  This is usually due to sudden trauma and often needs to be "reduced", or put back into place in an Emergency room. It can also happen after a seizure.<br>
  <br>

  <h5>SHOULDER SEPARATION</h5>
  A separation of the shoulder is very different from a dislocation since it involves the small joint at the end of the collar bone called the acromioclavicular joint or the "AC joint". Since we are the team physicians for the Milwaukee Admirals, we see lots of this injury since it's so common in professional hockey. It can also happen with other sports or activities that involve a fall onto the shoulder.<br>
  <br>

  <h5>SLAP TEARS</h5>
  The lining of the socket of the shoulder has a cartilage structure surrounding it called the labrum. The labrum can tear following a traumatic dislocation, but since the labrum goes 360 degrees around the shoulder, it can tear in any location. It usually tears in the direction of the dislocation. A "SLAP" tear simply describes where the labrum is torn. When the Superior (top part) Labrum tears from Anterior (front) to Posterior (back), we call it a SLAP tear. This is a category of its own because it can be seen without a dislocation or subluxation.<br>
  <br>

  <h5>FROZEN SHOULDER/ADHESIVE CAPSULITIS</h5>
  Sometimes the shoulder tightens up and loses motion resulting in a frozen shoulder. It can be due to trauma, but can also occur without any injury. The hallmarks of this condition include pain and stiffness (loss of motion). This is more common in diabetic patients and can be more difficult to treat in diabetics, but is seen in non-diabetics as well. Note that if you have this and you are not diabetic, it doesn't mean that you are more likely to get diabetes.<br>
  <br>
  <br>

