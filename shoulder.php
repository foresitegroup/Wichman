  <h4>ARTHRITIS</h4>
  Unfortunately, arthritis can involve any joint in our bodies. It is less common in non weight-bearing joints like the shoulder or elbow than in knees or hips, but the rate of arthritis of the shoulder is on the incline. We really don't know the causes behind the increased incidence. It involves loss of the smooth glistening surface of the joint. Sometimes a small area can lose its cartilage and other times, larger areas lose all the cartilage and the shoulder becomes "bone on bone". If a young athlete has an injury to the shoulder and loses a section of cartilage it is treated very differently than an older person where there is truly bone on bone arthritis.<br>
  <br>

  These conditions can occur by themselves or in combination. One of the most difficult problems to treat is when a person has arthritis along with a torn rotator cuff. Fortunately, there have been new developments in this area offering patients far more choices in treatment.
