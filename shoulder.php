  <h4>ROTATOR CUFF</h4>
  The rotator cuff is a group of four tendons that give strength to the shoulder and assist in raising the arm up and rotation of the shoulder. The following conditions involve the rotator cuff and often cause pain in addition to weakness.<br>
  <br>
  <br>

  <h5>Impingement and Rotator Cuff Tendonitis</h5>
  These are often synonymous. It can be an early precursor to a rotator cuff tear. The term "impingement" often refers to a bone spur rubbing on the surface of the rotator cuff causing pain.<br>
  <br>

  <h5>Rotator Cuff Tears</h5>
  These can be partial, full-thickness, small, medium, or even massive. They can be due to sudden injury (acute tears) or due to long-standing overuse (chronic tears).<br>
  <br>

  <h5>Calcific Tendonitis</h5>
  This condition occurs when a calcium deposit forms within the rotator cuff tendon. The cause of this is unknown, but it can present itself as relatively sudden onset of severe pain, usually with no history of injury.<br>
  <br>
  <br>

  <hr>
