  <h5>MENISCUS TEAR</h5>
  These can be sudden (acute) or chronic. They are usually the result of injury in people younger than 40, but often occur in patients over 40 without any trauma at all. We have a medial (inner) and a lateral (outer) meniscus. Each can suffer injury.<br>
  <br>

  <h5>ARTICULAR CARTILAGE INJURY</h5>
  Some of that glistening cartilage inside the knee can be damaged by injury or sometimes by developmental or genetic conditions. This can be very challenging problems, particularly in younger patients. The areas most commonly involved include the end of the femur as well as the under-surface of the patella (knee cap).<br>
  <br>

  <h5>MALALIGMENT</h5>
  When a joint such as the knee is malaligned or crooked, the surfaces can wear out more quickly or simply cause pain. This can be seen with bowed legs or knock knees (the opposite of bowed legs). Bowed legs tend to have problems with the inner side of the knee whereas "knock knees" tend to have more problems with the outer or lateral compartment. When damage occurs to those compartments, sometimes we need to straighten out the knee in order to get proper healing of the damaged area. This also occurs when the patella (kneecap) is not aligned properly. This can cause early wear on the cartilage as well as pain and sometimes a tendency for the kneecap to be unstable.<br>
