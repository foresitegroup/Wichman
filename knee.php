  <h4>KNEE INSTABILITY</h4>
  Instability of the knee. The knee can become unstable or loose following an injury. Often the knee will swell within a few hours and over the next few days. It may feel like it will "give out". We recommend that if you have an injury where the knee gives out and becomes swollen, you should be evaluated by a physician before returning to activities. Examples of ligament injuries in the knee include:<br>
  <br>
  <br>

  <h5>(ACL) Anterior Cruciate Ligament</h5>
  We read about these injuries in the Sports section all the time.<br>
  <br>

  <h5>(PCL) Posterior Cruciate Ligament</h5>
  These are much less common.<br>
  <br>

  <h5>(MCL) Medial Collateral Ligament</h5>
  These are very common as well, but usually heal on their own when they occur as an isolated injury.<br>
  <br>

  <h5>Patellar (Knee Cap) Instability</h5>
  There can be either subluxation (partial dislocation) or full dislocation. This often is the result of a torn MPFL (Medial Patello Femoral Ligament).<br>
