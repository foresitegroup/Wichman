  <h3>PREPARING FOR SURGERY?</h3>

  <h4>When will I find out my surgical time?</h4>
  The appropriate surgical facility will give you a call the day before surgery to notify you of your arrival time/surgical time.<br>
  <br>
  <br>

  <h4>What should I do to prepare for my surgery?</h4>
  <ol>
    <li>Do NOT eat or drink anything after midnight prior to your surgical date</li>
    <li>Arrange for a family member or friend to accompany you to the hospital the day of your surgery.</li>
    <li>Plan ahead for transportation home the day of your planned discharge.</li>
    <li>Remember to adjust your work/social schedule accordingly during your anticipated recovery time.</li>
    <li>While taking narcotic pain medication, you will not be permitted to drive. You may need to arrange for transportation to your initial follow-up visit.</li>
    <li>Unless told otherwise, you will need to have a follow-up visit 7-14 days after suture removal.</li>
    <li>You may see Dr. Wichman or a physician assistant for your first post-op appointment depending on the nature of the procedure performed or based on your preference.</li>
  </ol>
  <br>

  <h4>What should I bring with me the day of surgery?</h4>
  <ol>
    <li>Photo ID</li>
    <li>Insurance Card</li>
    <li>Friend or family member who will be available the entire time and take you home after surgery</li>
    <li>
      Wear comfortable, loose fitting clothing
      <ul>
        <li>Shoulder/elbow surgery: zip-up or button down shirt</li>
        <li>Knee surgery: loose fitting pants or shorts</li>
      </ul>
    </li>
  </ol>
  <strong><em>Leave jewelry, money and valuables at home.</em></strong><br>
  <br>
  <br>

  <h4>Medications to Stop Before Surgery</h4>
  <strong>14 days before surgery, you need to stop:</strong><br>
  <ul>
    <li>Any narcotic (example., Vicodin, Norco, Darvocet, Percocet or Oxycontin)</li>
    <li>Advil to aspirin like products to stop 14 days before surgery</li>
  </ul>
  <br>

  <strong>7 - 10 days before, you need to stop:</strong><br>
  Any medications prescribed by your physician, please consult before stopping prior surgery (example: Plavix, Coumadin, Warfarin, prescribed Aspirin)<br>
  <br>

  <strong>You can continue to take:</strong><br>
  <ul>
    <li>Tylenol</li>
    <li>Celebrex</li>
    <li>Glucosamine Chondroitin Sulfate</li>
    <li>Daily Vitamins</li>
  </ul>
  <br>
  <br>

  <h4>On the morning of surgery:</h4>
  <ul>
    <li>Take only medications that are for your heart (hypertension, arrhythmias, etc.)</li>
    <li>If you use an inhaler, please bring it with you</li>
    <li>If you are taking diabetic medications, you should check with your internist to determine if you should take these medications on the morning of surgery</li>
  </ul>
  <br>

  If you are taking any other medications that are not listed, please consult with your internist prior to surgery to determine if you should continue taking the medication or to see when you should stop and resume the medication.<br>
  <br>
  <br>

  <h4>Anesthesia</h4>

  <strong>General</strong><br>
  General anesthesia is used for many types of major surgery. During general anesthesia the entire body, including the brain is anesthetized. The patient has no awareness, feels nothing and remembers nothing of the surgical experience afterward. General anesthesia is administered by injecting a liquid anesthetic into a vein, or by breathing a gas anesthetic flowing from an anesthesia machine to the patient through a mask or tube. A plastic endotracheal tube or a mask placed over the airway is frequently used to administer gas anesthetics. With the tube in place, the airway is protected from aspiration of stomach fluids into the lungs. It is normal to have a slight sore throat after your surgery.<br>
  <br>

  <strong>Regional</strong><br>
  Injection of anesthetic (into the neck region for shoulder and elbow surgery, into the groin for some hip surgeries and the leg for knee surgeries) blocks pain impulses before they reach the brain. With this anesthetic, mental alertness is unaffected. Sedation, or even sleep may be offered to make you comfortable throughout the surgical experience. To receive the injection, you lie down while the anesthetic is injected into the neck or shoulder region. To make placement of the needle almost painless, your skin is first numbed with local anesthetic. This anesthetic may last for 6 to 8 hours and sometimes longer. It is important to start taking your postoperative pain medicine as soon as you begin to feel the onset of discomfort or when the numbness begins to wear off.<br>
  <br>

  <strong><em>Your anesthesiologist will speak with you directly prior to surgery to review your choice of anesthesia.</em></strong>
