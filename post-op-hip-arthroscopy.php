<div class="site-width patient-care">
  <h4>Diet</h4>
  <ul>
    <li>Begin with clear liquids and light foods (jello, soup, toast, etc...)</li>
    <li>Progress to your normal diet as tolerated</li>
    <li>Try to avoid taking narcotics on an empty stomach to avoid nausea</li>
  </ul>
  <br>
  <br>

  <h4>Wound Care</h4>
  <ul>
    <li>Take good care of your operative dressing.</li>
    <li>
      It is normal for the hip to bleed slightly and swell after surgery
      <ul>
        <li>If blood soaks through bandage, do no panic. Reinforce with additional dressings as needed.</li>
      </ul>
    </li>
    <li>
      Remove your surgical dressing on <strong><em>second</em></strong> post-operative day
      <ul>
        <li>If steri-strips are present, they should remain in place until first post-operative visit</li>
        <li>If minimal drainage is present, cover incision with band-aids and change daily.</li>
      </ul>
    </li>
    <li>To avoid infections, keep surgical incisions <strong><em>clean and dry</em></strong>. Please cover incisions with waterproof bandages, garbage bag or Press-and-Seal for showering.   NO immersion of leg in water (ie: bath or pool)</li>
  </ul>
  <br>
  <br>

  <h4>Medications</h4>
  <ul>
    <li>Pain medication is injected into the wound and shoulder joint during surgery.  This will wear off in approximately 8-12 hours from surgery.</li>
    <li>
      Most patients will require a short period of narcotic pain medication.  This should be taken as directed on the bottle and <strong>may stop when pain improves</strong>.
      <ul>
        <li>
          Norco (hydrocodone/tylenol) or Percocet (oxycodone/Tylenol)
          <ul>
            <li>Take 1-2 tablets every 4-6 hours <strong><em>as needed</em></strong> for pain.</li>
            <li>Maximum of 12 pills per 24 hour period</li>
            <li>Do NOT take additional Tylenol (acetaminophen) while taking these medications. Many products contain this medication (cough meds, cold meds) and can lead to liver damage.</li>
            <li>Common side effects of narcotic pain medications are nausea, drowsiness, and constipation.  Use a stool softener (Colace, senokot) or laxative (Miralax) as needed</li>
          </ul>
        </li>
        <li>MS Contin (15mg) <em>may</em> be prescribed.  This is a long-acting narcotic and should not be taken more frequently than every 12 hours.</li>
        <li>Aspirin 325mg should be taken daily (as directed) to gently thin the blood and decrease your risk of a DVT/Blood clot.</li>
        <li>Toradol (ketorolac) 10mg every 6 hours for two days. This will help with pain and reduce risk of heterotopic ossification (extra bone formation).</li>
        <li>Diclofenac sodium 75mg every 12 hours AFTER Toradol is complete.  Take for 1 month to also reduce risk of heterotopic ossification.</li>
        <li>If you are having problems with nausea and vomiting, call the office to see if the medication should be changed or additional medications prescribed.</li>
        <li><strong>Do not drive a car or operate heavy machinery while taking narcotics.</strong></li>
      </ul>
    </li>
  </ul>
  <br>
  <br>

  <h4>Activity</h4>
  <ul>
    <li>Avoid activities that increase pain/swelling until first post-operative visit. Also avoid prolonged standing or walking.</li>
    <li>Use crutches to assist with walking. <strong>50% weight-bearing should</strong> (Toe-touch weight-bearing for microfracture) should be followed for 3 weeks after surgery.</li>
    <li>Avoid long periods of sitting (without leg elevated) for any long distance travelling for the first two weeks after surgery.</li>
    <li>No driving until instructed by medical team</li>
    <li>You may return to sedentary work or school 3-4 days after surgery, if pain is tolerable.</li>
  </ul>
  <br>
  <br>

  <h4>Ice Therapy</h4>
  <ul>
    <li>Begin immediately after surgery</li>
    <li>Use ice machine or ice packs every 2 hours for 20 minutes until your first post-operative visit.</li>
    <li>If an ice machine was prescribed, may use continuously until first post-operative visit.  Remember to protect the skin to avoid thermal injury (frostbite)</li>
  </ul>
  <br>
  <br>

  <h4>Exercise</h4>
  <ul>
    <li>Begin exercises on post-operative day one.  (Straight leg raises, quad sets, heel slides and ankle pumps)</li>
    <li>Complete exercises 3-4 times daily until your first post-operative visit.  Your goal is to have complete extension (fully straight) and 90 degrees of flexion (bending) by your first post-operative visit (unless instructed otherwise)</li>
    <li>Perform ankle pumps throughout the day to reduce risk of blood clot in your calf.</li>
    <li>Formal physical therapy (PT) will be discussed at your first post-operative visit.</li>
  </ul>
  <br>
  <br>

  <h4>Continuous Passive Motion (CPM) machine</h4>
  <ul>
    <li>You will have been prescribed a CPM machine. Please begin this post-operative day 1. Advance as tolerated up to about 90 degrees.</li>
    <li>For most procedures, we recommend 4-6 hours daily. This can be broken into 2 or 3 sessions.</li>
    <li>For microfracture or other cartilage procedures, this will be increased to 6-8 hours daily.</li>
  </ul>

  <hr>

  <h3>EMERGENCIES</h3>
  Contact Dr. Wichman at 414-479-7000 if any of the following are present:<br>
  <ul>
    <li>Painful swelling or numbness</li>
    <li>Unrelenting pain</li>
    <li>Redness around incisions</li>
    <li>Fever (above 101.5&deg; - It is not uncommon to have a low-grade fever for the first day or two following surgery.</li>
    <li>Color changes in wrist, hand or lower extremity</li>
    <li>Continuous drainage or bleeding from incisions (a small amount of drainage is expected.)</li>
    <li>Difficulty breathing</li>
    <li>Excessive nausea or vomiting</li>
    <li style="color: #F15928; font-weight: bold; font-style: italic;">If you have an emergency after hours or over the weekend, call 414-479-7000 to be connected to the "on-call" physician or physician assistant.  Do NOT call the hospital or surgery center.</li>
    <li style="color: #F15928; font-weight: bold; font-style: italic;">If you have an emergency that requires immediate attention, call 911 or proceed to the nearest Emergency Room.</li>
  </ul>
